package loop.ex;

public class WhileEx1 {

    public static void main(String[] args) {
        /**
         * 처음 10개의 자연수를 출력
         * 변수는 int형 count
         */

        int count = 1;

        while (count <= 10) {
            System.out.println(count);
            count++;
        }
    }


}

